    public function showProducts($material_id)
    {
        $products = MaterialProduct::query()
            ->where("material_products.material_id", $material_id)
            ->leftJoin("products", "material_products.product_id", "=", "products.id")
            ->select('products.*', 'material_products.quantity')
            ->get();
        return response()->json(['products' => $products]);
    }

